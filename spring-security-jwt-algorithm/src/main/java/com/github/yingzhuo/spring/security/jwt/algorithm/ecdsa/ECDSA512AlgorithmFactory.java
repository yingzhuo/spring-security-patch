package com.github.yingzhuo.spring.security.jwt.algorithm.ecdsa;

import com.auth0.jwt.algorithms.Algorithm;

import java.util.Objects;

/**
 * @author 应卓
 * @since 1.1.0
 */
public class ECDSA512AlgorithmFactory extends AbstractECDSAAlgorithmFactory {

    private final String publicKey;
    private final String privateKey;

    public ECDSA512AlgorithmFactory(String publicKey, String privateKey) {
        this.publicKey = Objects.requireNonNull(publicKey);
        this.privateKey = Objects.requireNonNull(privateKey);
    }

    @Override
    public Algorithm create() {
        return Algorithm.ECDSA512(toPublicKey(publicKey), toPrivateKey(privateKey));
    }

}
